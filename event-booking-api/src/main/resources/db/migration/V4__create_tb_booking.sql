CREATE TABLE TB_BOOKING(
    ID_BOOKING INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_USER INTEGER NOT NULL,
    ID_EVENT INTEGER NOT NULL,
    NUMBER_TICKETS INTEGER NOT NULL,
    CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT FK_BOOKING_USER FOREIGN KEY(ID_USER) REFERENCES TB_USER(ID_USER),
    CONSTRAINT FK_BOOKING_EVENT FOREIGN KEY(ID_EVENT) REFERENCES TB_EVENT(ID_EVENT)
);